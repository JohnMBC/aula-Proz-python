# Crie um programa que receba dois numero do usuario e exiba a soma, subtração, multiplicação e divisão dos dois numeros

numero1= int(input("Digite um numero: "))
numero2= int(input("Digite um numero: "))
print(numero1 + numero2)
print(numero1 - numero2)
print(numero1 * numero2)
print(numero1 / numero2)

